package com.actorfw.infra.modules.codegroup;

public class CodeGroup {
	
	private Integer seq;
	private Integer codeseq;
	private String name;
	private String codename;
	private Integer oder;
	private Integer useNy;
	private Integer delNy;
	private Integer codeCount;
	
	
	
//-----------------------------------------------------	
	
	public Integer getSeq() {
		return seq;
	}
	public void setSeq(Integer seq) {
		this.seq = seq;
	}
	public Integer getCodeseq() {
		return codeseq;
	}
	public void setCodeseq(Integer codeseq) {
		this.codeseq = codeseq;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getCodename() {
		return codename;
	}
	public void setCodename(String codename) {
		this.codename = codename;
	}
	public Integer getOder() {
		return oder;
	}
	public void setOder(Integer oder) {
		this.oder = oder;
	}
	public Integer getUseNy() {
		return useNy;
	}
	public void setUseNy(Integer useNy) {
		this.useNy = useNy;
	}
	public Integer getDelNy() {
		return delNy;
	}
	public void setDelNy(Integer delNy) {
		this.delNy = delNy;
	}
	public Integer getCodeCount() {
		return codeCount;
	}
	public void setCodeCount(Integer codeCount) {
		this.codeCount = codeCount;
	}
	
	
	

}
